#!/usr/bin/env ruby
# frozen_string_literal: true

# Render every run record in docs/results/*.json into docs/BENCHMARKS.md.
#
# Kept separate from collect.rb so the table can be re-rendered (after a price
# change, a new SLO, a hand-edited note) without paying for another load run.
#
#   ruby bench/aggregate.rb

require "json"
require "time"
require "fileutils"

module Aggregate
  module_function

  ROOT        = File.expand_path("..", __dir__)
  RESULTS_DIR = File.join(ROOT, "docs", "results")
  OUTPUT      = File.join(ROOT, "docs", "BENCHMARKS.md")

  # Fixed order so a scenario keeps its place in the doc as runs come and go.
  SCENARIOS = %w[cpu io db_read db_write db_latency mixed].freeze

  def records
    Dir[File.join(RESULTS_DIR, "*.json")].sort.map { |path| JSON.parse(File.read(path)) }
  end

  def write(recs = records)
    FileUtils.mkdir_p(File.dirname(OUTPUT))
    File.write(OUTPUT, render(recs))
    OUTPUT
  end

  def render(recs)
    [
      header(recs),
      how_to_read,
      scenario_tables(recs),
      run_details(recs)
    ].compact.join("\n")
  end

  def header(recs)
    platforms = recs.map { |r| r["platform"] }.uniq.sort
    <<~MD
      # OptRails benchmark results

      <!-- Generated by bench/aggregate.rb from docs/results/*.json.
           Do not edit by hand: re-run `ruby bench/aggregate.rb`. -->

      #{recs.size} run(s) across #{platforms.size} platform(s): #{platforms.join(', ')}.
      Last rendered #{Time.now.utc.iso8601}.
    MD
  end

  def how_to_read
    <<~MD

      ## How to read this

      The question is **sustained requests/sec under a p95 latency SLO, per dollar
      per month**. Two rules make the numbers honest, and both bite:

      - **RPS only counts if the SLO actually held.** A run whose p95 broke the SLO
        scores **0 RPS/$**, not "high RPS with bad latency" — otherwise a box that
        answers fast-and-wrong outranks one that answers correctly. Same rule as
        `bench/results/crunch.py` (`rps_eff`).
      - **Cost is shown two ways, and they can disagree.** `RPS/$` prices the
        compute tier alone (from `bench/prices.json`) and is the like-for-like
        comparison the README documents. `RPS/$ total` adds the managed database,
        which is what the bill actually says — but it flatters a platform that
        bundles a cheaper, weaker DB. When the two columns rank platforms
        differently, that disagreement *is* the finding; read both.

      `workers x threads` is what the autotuner actually chose at boot, read from
      `/bench/info`. Every RPS number should be explainable by it.
    MD
  end

  def scenario_tables(recs)
    out = +"\n## RPS per dollar\n"
    present = SCENARIOS.select { |s| recs.any? { |r| r.dig("scenarios", s) } }

    if present.empty?
      return out + "\nNo load runs recorded yet — every run so far is topology-only " \
                   "(`--no-load`). Install k6 and re-run `bench/collect.rb` to fill this in.\n"
    end

    present.each do |scenario|
      rows = recs.filter_map do |r|
        s = r.dig("scenarios", scenario)
        next unless s

        [r, s]
      end
      # Best value first; a broken SLO scores 0 and sinks to the bottom.
      rows.sort_by! { |_r, s| -(s["rps_per_usd"] || 0) }

      out << "\n### `#{scenario}` — SLO p95 < #{rows.first[0]['slo_ms']} ms\n\n"
      out << "| platform | tier | database | RAM | $/mo | +db $/mo | workers x threads | " \
             "sustained RPS | p95 ms | SLO held | RPS/$ | RPS/$ total |\n"
      out << "|---|---|---|---:|---:|---:|---:|---:|---:|:---:|---:|---:|\n"
      rows.each { |r, s| out << scenario_row(r, s) }
    end
    out
  end

  def scenario_row(rec, s)
    price = rec["price"] || {}
    info  = rec["info"] || {}
    plan  = "#{info['workers'] || '?'} x #{info['threads'] || '?'}"
    ram   = price["ram_gb"] ? "#{price['ram_gb']} GB" : "—"

    held = s["met_slo"]
    # The sustained figure is the achieved rate at the knee, not the rate we
    # asked for — a struggling box accepts fewer requests than it is offered.
    sustained = held ? num(s["rps"], 1) : "0"
    sustained += " (>=)" if s["knee_not_found"]

    # Two runs of the same tier can differ only by their database plan, so it has
    # to be in the table or the rows are indistinguishable.
    db = rec.dig("db", "plan") || "—"

    format("| %s | %s | %s | %s | %s | %s | %s | %s | %s | %s | %s | %s |\n",
           rec["platform"], rec["tier"], db, ram,
           usd(price["usd_month"]), usd(price["usd_month_total"]), plan,
           sustained, num(s["p95_ms"], 1),
           held ? "yes" : "**no**",
           num(s["rps_per_usd"], 2), num(s["rps_per_usd_total"], 2))
  end

  def usd(value)
    value ? format("$%.2f", value) : "—"
  end

  def run_details(recs)
    out = +"\n## Runs\n"
    recs.sort_by { |r| r["collected_at"].to_s }.reverse_each do |r|
      info = r["info"] || {}
      out << "\n### #{r['platform']} / #{r['tier']} — #{r['collected_at']}\n\n"
      out << "- **URL**: #{r['url']}\n"
      out << "- **Autotune**: #{info['workers']} workers x #{info['threads']} threads, " \
             "#{info['total_mem_mb']} MB, #{info['cpu_count']} vCPU reported, " \
             "#{info['worker_rss_mb']} MB RSS/worker\n"
      out << "- **Stack**: Ruby #{info['ruby']}, Rails #{info['rails']}\n"
      out << "- **DB host**: `#{info['db_host']}`\n" if info["db_host"]
      if (db = r["db"]) && db["plan"]
        cost = db["usd_month"] ? format(" (~$%.2f/mo)", db["usd_month"]) : ""
        out << "- **Database**: #{db['plan']}#{cost}\n"
      end
      out << "- **WORKER_RSS_MB**: #{r.dig('env', 'worker_rss_mb') || 'unset'}" \
             "#{' (placeholder — not measured)' if r.dig('env', 'worker_rss_mb').to_s == '300'}\n"
      out << "- **Commit**: `#{r['git_sha']}`\n" if r["git_sha"]
      if (load = r["load"])
        out << "- **Load**: fixed rates #{Array(load['rates']).join(', ')} RPS, " \
               "#{load['duration']}s each, driven from #{load['driven_from'] || 'unspecified'}\n"
      else
        out << "- **Load**: none — topology only\n"
      end
      Array(r["notes"]).each { |n| out << "- **Note**: #{n}\n" }
      out << ladder(r)
    end
    out
  end

  # The rate-by-rate curve behind each headline number. Without it "31 RPS" is
  # unfalsifiable: you cannot tell a box that degraded gracefully from one that
  # fell off a cliff one step later.
  def ladder(rec)
    steps = (rec["scenarios"] || {}).reject { |_k, v| Array(v["ladder"]).empty? }
    return "" if steps.empty?

    out = +"\n<details><summary>Rate ladder — p95 at each fixed rate, " \
           "as wall time (app time + queue/network)</summary>\n\n"
    steps.each do |scenario, s|
      curve = s["ladder"].map do |step|
        mark = step["met_slo"] ? "ok" : "**BROKE**"
        wall = format("%.0f", step["p95_ms"] || 0)
        split = if step["server_p95_ms"]
                  format(" (app %.0f + wait %.0f)", step["server_p95_ms"],
                         step["queue_and_network_p95_ms"] || 0)
                else
                  ""
                end
        "#{step['target_rps']} RPS: #{wall} ms#{split} #{mark}"
      end
      out << "- **#{scenario}**\n"
      curve.each { |c| out << "  - #{c}\n" }
    end
    out << "\n</details>\n"
  end

  def num(value, places)
    value ? format("%.#{places}f", value) : "—"
  end
end

Aggregate.write if $PROGRAM_NAME == __FILE__
